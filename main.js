function mobileMenu() {
    const nonDisplay = document.getElementById('mobile-menu')
    const blur = document.getElementById('blur')
   
    blur.classList.toggle("blur")
    nonDisplay.classList.toggle('non-display')
   }
   
   function removeBlur() {
    const nonDisplay = document.getElementById('mobile-menu')
    const blur = document.getElementById('blur')
   
    blur.classList.toggle("blur")
    nonDisplay.classList.toggle('non-display')
   }